crime_data <- read.table("C:/Users/Amita Kamat/Documents/R/crime.dat", header=T, sep="\t")
max_y <- max(crime_data)
colors <- c("blue","red","darkgreen", "black")
png(filename="C:/Users/Amita Kamat/Documents/R/crime-analysis.png", height=1000, width=1000, bg="white")
plot(crime_data$On.Campus, type="o", col=colors[1],  ylim=c(0,max_y), axes=FALSE, ann=FALSE, lwd=3)
